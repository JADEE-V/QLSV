private void loadTable() {
    DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
    model.setRowCount(0); // xóa dữ liệu cũ

    List<String[]> students = StudentDAO.getAllStudents(); // lấy từ DB
    for (String[] row : students) {
        model.addRow(row);
    }
}

