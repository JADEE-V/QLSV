    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
                                 
    String maSV = jTextField1.getText();

    if(maSV.isEmpty()){
        javax.swing.JOptionPane.showMessageDialog(this, "Nhập mã sinh viên cần xóa!");
        return;
    }

    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa?", "Xác nhận", javax.swing.JOptionPane.YES_NO_OPTION);
    if(confirm == javax.swing.JOptionPane.YES_OPTION){
        boolean ok = StudentDAO.deleteStudent(maSV);
        if(ok){
            javax.swing.JOptionPane.showMessageDialog(this, "Xóa thành công!");
            loadTable();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Xóa thất bại!");
        }
    }

    }//GEN-LAST:event_jButton3ActionPerformed

